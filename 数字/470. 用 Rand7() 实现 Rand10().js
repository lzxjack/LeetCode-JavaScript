const rand10 = () => {
    let m, n, num;
    do {
        m = rand7();
        n = rand7();
        // 使生成的[1,49]等概率
        num = m + (n - 1) * 7;
    } while (num > 40);
    // 将[1,40]转化为[1,10]
    return 1 + ((num - 1) % 10);
};

// 等概率生成[1,49]
// 若生成的数大于40，则舍弃，重新生成，一直到生成[1,40]之间的数位置
